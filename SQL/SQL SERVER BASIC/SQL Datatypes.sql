
### ১. String Data (টেক্সট ডেটা)
এটি মূলত অক্ষর বা লেখা সংরক্ষণের জন্য ব্যবহৃত হয়।
* **`char`**: ফিক্সড লেন্থ। যেমন: লিঙ্গ (M বা F) এর জন্য `char(1)`।
* **`varchar`**: পরিবর্তনশীল লেন্থ (Variable length)। যেমন: নাম বা ইমেইল।
* **`nchar` & `nvarchar`**: এগুলো **Unicode** সাপোর্ট করে। অর্থাৎ, ইংরেজি ছাড়া অন্যান্য ভাষা (যেমন বাংলা বা ইমোজি) সংরক্ষণের জন্য এগুলো সেরা।

### ২. Number Type (সংখ্যার ডেটা)
সংখ্যার পরিমাণের ওপর ভিত্তি করে এগুলো আলাদা হয়:
* **`tinyint`**: খুব ছোট সংখ্যা (যেমন: ছাত্রের বয়স)।
* **`smallint`**: মাঝারি সংখ্যা (যেমন: গাছ বা বিল্ডিংয়ের বয়স)।
* **`int` & `bigint`**: বড় এবং বিশাল পূর্ণসংখ্যার জন্য।
* **`decimal(p,s)`**: দশমিক সংখ্যার জন্য। 
    * **p (Precision):** মোট কয়টি সংখ্যা থাকবে।
    * **s (Scale):** দশমিকের পরে কয়টি সংখ্যা থাকবে। 
    * *উদাহরণ:* `999.99` লিখতে হবে `decimal(5,2)`।

### ৩. Date & Time (তারিখ ও সময়)
* **`date`**: শুধুমাত্র তারিখ (YYYY-MM-DD)।
* **`time`**: শুধুমাত্র সময় (HH:MM:SS)।
* **`datetime`**: তারিখ এবং সময় একসাথে সংরক্ষণের জন্য।

**সারকথা:** ডেটার ধরন এবং সাইজ অনুযায়ী সঠিক টাইপ সিলেক্ট করলে ডাটাবেস দ্রুত কাজ করে এবং মেমোরি কম খরচ হয়।